"""Generate the zkx CPU codegen golden for the jagged-eval sumcheck kernel.

Runs the byte-match-tested reference (`observe(claimed_sum)` then
`_run_inner_rounds`) on a tiny instance with the koalabear16 poseidon2
transcript, and prints a C++ header for
`zkx/backends/cpu/codegen/testdata/sumcheck_jagged_eval_golden.h`. The kernel's
`EmitJaggedEvalRounds` must reproduce this flat output byte-for-byte:
`[challenges][round_polys][folded][in_buffer][out_buffer][sponge]`.

Run: `bazel run //sp1_zorch/jagged:gen_jagged_eval_golden`.
"""

from __future__ import annotations

import jax
import jax.numpy as jnp
import numpy as np
from zk_dtypes import koalabear_mont as BF
from zk_dtypes import koalabearx4_mont as EF

from sp1_zorch.jagged.prover import _bp_all, _run_inner_rounds, merged_prefix_bits
from sp1_zorch.poseidon2.koalabear16 import koalabear16_params
from sp1_zorch.shard_prover.replay import fresh_transcript
from zorch.pcs.jagged.poly import _TRANSITION_ROWS, build_jagged_layout
from zorch.poly.eq import expand_eq_to_hypercube
from zorch.poly.univariate import eval_coeffs

# Smallest instance that still exercises the bit-order / matvec / column
# reduction / observe-sample-fold (n_d=1 -> 1 BP layer, 2 rounds, 2 columns):
# the dbg-build CPU runtime-compile of the unrolled BP DP is the cost, so keep
# it minimal.
_COL_HEIGHTS = [1, 1]
_N_R = 1


def _ef(seed: int, n: int):
    """`n` distinct in-range koalabearx4 elements (arbitrary but valid: any u32
    below the prime is a valid Montgomery limb)."""
    limbs = (np.arange(n * 4, dtype=np.int64) * 7919 + seed * 104729) % 2000003 + 1
    return jax.lax.bitcast_convert_type(
        jnp.asarray(limbs, dtype=jnp.uint32).reshape(n, 4), EF
    )


def _u32(arr) -> list[int]:
    """Canonical (non-Montgomery) u32 limbs of a field array, flattened. An
    extension element contributes its 4 base coefficients low-to-high (the
    FlattenSumcheckResult / ToBaseCoeffs order). The zkx test reconstructs each
    via KB(canonical)/X4(...), matching the existing sumcheck goldens' canonical
    encoding (NOT bitcast Montgomery storage)."""
    base = jax.lax.bitcast_convert_type(arr, BF) if arr.dtype == EF else arr
    canon = jnp.asarray(base).astype(jnp.uint32)
    return [int(x) for x in np.asarray(canon).reshape(-1)]


def _carr(name: str, vals: list[int]) -> str:
    body = ", ".join(f"{v}u" for v in vals)
    return f"inline constexpr uint32_t {name}[] = {{{body}}};"


def main() -> None:
    _, cfg = build_jagged_layout(_COL_HEIGHTS, len(_COL_HEIGHTS), _N_R, EF)
    num_bits = cfg.n_d
    n_vars = 2 * num_bits
    bp_num_vars = max(cfg.n_r, cfg.n_d)
    l_max = len(_COL_HEIGHTS)
    ef_limbs = 4

    z_row = _ef(11, cfg.n_r)
    z_col = _ef(22, cfg.n_c)
    z_trace = _ef(33, cfg.n_d)
    t_matrix = jnp.asarray(_TRANSITION_ROWS, dtype=EF)
    merged = merged_prefix_bits(_COL_HEIGHTS, num_bits, dtype=EF)
    weights = expand_eq_to_hypercube(z_col, jnp.ones((), EF))[:l_max]
    claimed_sum = jnp.sum(
        weights * _bp_all(merged, z_row, z_trace, t_matrix, num_bits, bp_num_vars)
    )

    # The kernel observes the claim before the rounds (SP1 #90) starting from a
    # fresh (0,0) sponge; mirror that here, then run the round loop. The test
    # feeds the round constants this transcript's poseidon2 uses (dumped below),
    # so the Fiat-Shamir sponge matches byte-for-byte.
    t = fresh_transcript()
    t = t.observe(claimed_sum)
    polys, challenges, t = _run_inner_rounds(
        merged,
        weights,
        claimed_sum,
        t,
        z_row=z_row,
        z_trace=z_trace,
        t_matrix=t_matrix,
        num_bits=num_bits,
        bp_num_vars=bp_num_vars,
        dtype=EF,
    )
    final_claim = eval_coeffs(polys[-1], challenges[-1])
    st = t.state  # DuplexState: input_buffer, output_buffer, sponge_state, ...

    print("/* Copyright 2026 The ZKX Authors. Apache-2.0. */")
    print(
        "// Auto-generated by //sp1_zorch/jagged:gen_jagged_eval_golden for "
        "sp1-zorch#144 -- DO NOT EDIT."
    )
    print(
        f"// Jagged point-eval sumcheck, col_heights={_COL_HEIGHTS} "
        f"(L={l_max}, n_r={cfg.n_r}, n_d={cfg.n_d}, n_vars={n_vars}, "
        f"bp_num_vars={bp_num_vars}), SP1 koalabear16 poseidon2 (rate 8/width "
        f"16; powers-of-two internal diag with R^-1 folded in + off_diag J "
        f"scale, distinct from sumcheck_golden's Plonky3 diagonal). Forward "
        f"challenges; deg-2 coeff polys."
    )
    print("#ifndef ZKX_BACKENDS_CPU_CODEGEN_TESTDATA_SUMCHECK_JAGGED_EVAL_GOLDEN_H_")
    print("#define ZKX_BACKENDS_CPU_CODEGEN_TESTDATA_SUMCHECK_JAGGED_EVAL_GOLDEN_H_")
    print("#include <cstdint>")
    print("namespace zkx::cpu::sumcheck_jagged_eval_golden {")
    print(f"inline constexpr int64_t kNumVars = {n_vars};")
    print(f"inline constexpr int64_t kNumBits = {num_bits};")
    print(f"inline constexpr int64_t kNr = {cfg.n_r};")
    print(f"inline constexpr int64_t kNd = {cfg.n_d};")
    print(f"inline constexpr int64_t kL = {l_max};")
    print(f"inline constexpr int64_t kChallengeLimbs = {ef_limbs};")
    print(
        "// --- inputs (operand order: merged, weights, claim, z_row, z_trace, t_matrix) ---"
    )
    print(_carr("kMerged", _u32(merged)))  # (L, 2*num_bits) EF
    print(_carr("kWeights", _u32(weights)))  # (L,) EF
    print(_carr("kClaim", _u32(claimed_sum)))  # scalar EF
    print(_carr("kZRow", _u32(z_row)))  # (n_r,) EF
    print(_carr("kZTrace", _u32(z_trace)))  # (n_d,) EF
    print(_carr("kTMatrix", _u32(t_matrix)))  # (64,4) EF
    # --- poseidon2 round constants (this transcript's; the duplex leaves are
    # fed as zeros). External rounds=4, internal=20, sbox=3, width=16, rate=8. ---
    pp = koalabear16_params()
    print(_carr("kExtInitRc", _u32(pp.external_constants_initial)))  # [E*W]
    print(_carr("kIntRc", _u32(pp.internal_constants[:, 0])))  # [I]
    print(_carr("kExtTermRc", _u32(pp.external_constants_terminal)))  # [E*W]
    print(_carr("kDiag", _u32(pp.internal_diag)))  # [W]
    # Internal-layer J scale (off_diag). SP1's DiffusionMatrixKoalaBear
    # permute_mut computes R^-1*M*state, so the diffusion's row-sum term carries
    # R^-1 (= internal_j_scale); the emitter applies it as scaled_sum =
    # off_diag*sum. Dumped as RAW field storage bits (the form the rewriter reads
    # via ScalarFieldRawBits and feeds to createFieldConstant), NOT canonical: 0
    # means identity. internal_diag is fed canonically as a field tensor, but
    # off_diag is a scalar baked into the backend config as Montgomery storage.
    off_diag_bits = int(
        np.asarray(
            jax.lax.bitcast_convert_type(jnp.asarray(pp.internal_j_scale), jnp.uint32)
        ).reshape(-1)[0]
    )
    print(f"inline constexpr uint32_t kOffDiag = {off_diag_bits}u;")
    print(
        "// --- outputs: [challenges][round_polys][folded][in_buf][out_buf][sponge] ---"
    )
    print(_carr("kChallenges", _u32(jnp.stack(list(challenges)))))
    print(_carr("kRoundPolys", _u32(jnp.stack(list(polys)))))
    print(_carr("kFolded", _u32(final_claim)))
    print(_carr("kInBuffer", _u32(st.input_buffer)))
    print(_carr("kOutBuffer", _u32(st.output_buffer)))
    print(_carr("kSponge", _u32(st.sponge_state)))
    print("}  // namespace zkx::cpu::sumcheck_jagged_eval_golden")
    print("#endif  // ZKX_BACKENDS_CPU_CODEGEN_TESTDATA_SUMCHECK_JAGGED_EVAL_GOLDEN_H_")


if __name__ == "__main__":
    main()
